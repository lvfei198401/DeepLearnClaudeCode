import { AGENT_TOOL_NAME } from '../../tools/AgentTool/constants.js'
import { registerBundledSkill } from '../bundledSkills.js'

// 简化提示词：代码审查与清理
const SIMPLIFY_PROMPT = `# 简化：代码审查与清理

审查所有变更文件的复用性、质量和效率。修复发现的所有问题。

## 第一阶段：识别变更内容

执行 \`git diff\`（若有暂存变更则执行 \`git diff HEAD\`）查看变更内容。如果没有Git变更，审查用户提及的或本次对话中你之前编辑过的最近修改文件。

## 第二阶段：并行启动三个审查代理

使用 ${AGENT_TOOL_NAME} 工具在单条消息中并发启动全部三个代理。为每个代理传递完整的差异内容，确保其拥有完整上下文。

### 代理1：代码复用审查

针对每一处变更：

1. **搜索现有工具方法和辅助函数**，替换新编写的代码。在代码库其他位置查找相似模式——常见位置包括工具目录、共享模块以及变更文件的相邻文件。
2. **标记所有重复现有功能的新函数**，建议改用现有函数。
3. **标记所有可使用现有工具的内联逻辑**——手动实现的字符串处理、路径操作、环境检查、类型守卫等场景均为重点排查对象。

### 代理2：代码质量审查

针对相同变更审查不规范的编码模式：

1. **冗余状态**：重复现有状态的状态、可推导的缓存值、可改为直接调用的监听器/副作用
2. **参数泛滥**：为函数新增参数，而非优化或重构现有参数
3. **轻微变体的复制粘贴代码**：应通过共享抽象统一的近似重复代码块
4. **抽象泄漏**：暴露本该封装的内部细节，或破坏现有抽象边界
5. **字符串强类型代码**：在代码库已存在常量、枚举（字符串联合类型）或品牌类型的场景下使用原始字符串
6. **不必要的JSX嵌套**：无布局价值的包裹容器/元素——检查内部组件属性（flexShrink、alignItems等）是否已提供所需行为
7. **不必要的注释**：解释代码功能的注释（语义化命名已能体现）、记录变更过程、关联任务/调用方的注释——删除此类注释；仅保留说明非直观原因的注释（隐藏约束、微妙不变量、临时解决方案）

### 代理3：代码效率审查

针对相同变更审查效率问题：

1. **无效操作**：冗余计算、重复文件读取、重复网络/接口调用、N+1查询模式
2. **并发缺失**：本可并行执行的独立操作被串行执行
3. **关键路径冗余**：在启动、每次请求/渲染的关键路径中新增阻塞操作
4. **重复空操作更新**：轮询循环、定时器、事件处理器中无条件触发的状态/存储更新——添加变更检测逻辑，避免无变更时通知下游使用者。此外：若包裹函数接收更新器/归约回调，验证其是否支持相同引用返回（或无变更信号）——否则调用方的提前返回空操作会被静默失效
5. **不必要的存在性检查**：操作前预先检查文件/资源是否存在（检查-使用-检查反模式）——直接操作并处理错误即可
6. **内存问题**：无界数据结构、缺少清理逻辑、事件监听器泄漏
7. **过宽范围操作**：仅需部分内容却读取整个文件、仅需筛选单个数据却加载全部数据

## 第三阶段：修复问题

等待全部三个代理执行完成。汇总审查结果并直接修复每个问题。若某条结果为误报或无需处理，记录后跳过——无需争论结果，直接跳过即可。

完成后，简要总结修复内容（或确认代码已符合规范）。
`

/**
 * 注册简化技能
 * 用于注册代码简化的内置技能，提供代码审查、优化修复能力
 */
export function registerSimplifySkill(): void {
  registerBundledSkill({
    name: 'simplify',
    description:
      '审查变更代码的复用性、质量和效率，然后修复发现的所有问题。',
    userInvocable: true,
    async getPromptForCommand(args) {
      let prompt = SIMPLIFY_PROMPT
      if (args) {
        prompt += `\n\n## 额外关注重点\n\n${args}`
      }
      return [{ type: 'text', text: prompt }]
    },
  })
}