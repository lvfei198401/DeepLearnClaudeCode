import { isAutoMemoryEnabled } from '../../memdir/paths.js'
import { registerBundledSkill } from '../bundledSkills.js'

/**
 * 注册记忆整理技能
 */
export function registerRememberSkill(): void {
  // 仅对蚂蚁用户开放该技能
  if (process.env.USER_TYPE !== 'ant') {
    return
  }

  // 技能提示词模板：定义记忆整理的完整规则、流程和输出要求
  const SKILL_PROMPT = `# 记忆回顾

## 目标
回顾用户的记忆体系，生成一份清晰的修改建议报告，按操作类型分组。**请勿直接执行修改**——仅展示修改提案供用户确认。

## 执行步骤

### 1. 收集所有记忆层
读取项目根目录下的 CLAUDE.md 和 CLAUDE.local.md 文件（若存在）。自动记忆内容已包含在系统提示词中——直接在该处查阅。记录是否存在团队记忆板块。

**成功标准**：已获取所有记忆层的内容并可进行对比。

### 2. 分类每条自动记忆条目
针对自动记忆中的每条有效条目，确定最佳归属位置：

| 归属位置 | 适用内容 | 示例 |
|---|---|---|
| **CLAUDE.md** | 项目规范及所有协作者都应遵守的 Claude 使用说明 | "使用 bun 而非 npm"、"API 路由使用短横线命名"、"测试命令为 bun test"、"优先使用函数式编程风格" |
| **CLAUDE.local.md** | 仅适用于当前用户的个性化 Claude 使用说明，其他协作者无需遵循 | "我偏好简洁的回复"、"始终解释方案权衡"、"请勿自动提交代码"、"提交前运行测试" |
| **团队记忆** | 适用于全仓库的组织级通用知识（仅当配置了团队记忆时可用） | "部署 PR 需通过 #deploy-queue 队列"、"测试环境地址 staging.internal"、"基础设施由平台团队负责" |
| **保留在自动记忆** | 工作笔记、临时上下文或无法明确归类的条目 | 会话专属观察记录、不确定的规律总结 |

**重要区分规则**：
- CLAUDE.md 和 CLAUDE.local.md 仅存放 Claude 使用说明，**不包含**用户对外部工具的偏好设置（如编辑器主题、IDE 快捷键等）
- 工作流规范（PR 规范、合并策略、分支命名）属于模糊场景——需询问用户归属于个人还是团队
- 不确定时务必询问，切勿主观猜测

**成功标准**：每条条目都已确定建议归属位置，或标记为模糊待确认。

### 3. 识别可清理项
遍历所有记忆层，检查以下问题：
- **重复项**：自动记忆中已存在于 CLAUDE.md 或 CLAUDE.local.md 的条目→建议从自动记忆中移除
- **过期项**：CLAUDE.md 或 CLAUDE.local.md 中被新自动记忆条目推翻的内容→建议更新旧记忆层
- **冲突项**：任意两个记忆层之间存在矛盾→建议给出解决方案，并标注最新版本

**成功标准**：已识别所有跨记忆层问题。

### 4. 生成报告
输出结构化报告，按操作类型分组：
1. **迁移项**——待移动的条目，包含目标位置和理由
2. **清理项**——重复、过期、冲突的待处理条目
3. **待确认项**——需要用户明确归属的条目
4. **无需操作**——保留原位置的条目简要说明

若自动记忆为空，直接告知用户，并可主动提议检查 CLAUDE.md 进行清理。

**成功标准**：用户可逐一审核并确认/拒绝每条提案。

## 约束规则
- 执行任何修改前，必须先展示**全部提案**
- 未经用户明确许可，禁止修改任何文件
- 仅当目标文件不存在时，才可创建新文件
- 模糊条目必须询问，禁止主观猜测
`

  // 注册内置技能
  registerBundledSkill({
    name: 'remember',
    description:
      '回顾自动记忆条目，建议迁移至 CLAUDE.md、CLAUDE.local.md 或共享记忆。同时检测各记忆层中的过期、冲突、重复条目。',
    whenToUse:
      '当用户需要回顾、整理、迁移自动记忆条目时使用。也可用于清理 CLAUDE.md、CLAUDE.local.md 和自动记忆中的过期、冲突条目。',
    userInvocable: true,
    isEnabled: () => isAutoMemoryEnabled(),
    async getPromptForCommand(args) {
      let prompt = SKILL_PROMPT

      // 若用户提供了附加参数，追加到提示词末尾
      if (args) {
        prompt += `\n## 用户提供的附加上下文\n\n${args}`
      }

      return [{ type: 'text', text: prompt }]
    },
  })
}